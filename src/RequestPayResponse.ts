import { PaymentMethod } from './PaymentMethod';
import { Pg } from './Pg';

export interface RequestPayAdditionalResponse {
  apply_num?: string;
  vbank_num?: string;
  vbank_name?: string;
  vbank_holder?: string | null;
  vbank_date?: number;
}

export interface RequestPayResponse extends RequestPayAdditionalResponse {
  success: boolean;
  error_code?: string;
  error_msg?: string;
  imp_uid: string | null;
  merchant_uid: string;
  pay_method?: PaymentMethod;
  paid_amount?: number;
  status?: string;
  name?: string;
  pg_provider?: Pg;
  pg_tid?: string;
  buyer_name?: string;
  buyer_email?: string;
  buyer_tel?: string;
  buyer_addr?: string;
}

export type RequestPayResponseCallback = (response: RequestPayResponse) => void;
